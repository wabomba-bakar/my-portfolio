const mongoose = require('mongoose');
const schema = mongoose.Schema;

const MessageSchema = new schema({
    name: {
        type: String,
        required: true
    },
    email: {
        type: String,
        required: true
    },
    message: {
        type: String,
        required: true
    },
    date: {
        type: Date,
        default: Date.now
    }
});

mongoose.model('messages', MessageSchema);